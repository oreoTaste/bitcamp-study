package com.ohoracs.basic.ex03;

public class Exam1 {
  public static void main(String[] args){
    //정수 리터럴 (10진수)
    System.out.println(78);
    //정수 리터럴 (8진수)
    System.out.println(0116);
    System.out.println(-0116);

    //정수 리터럴 (16진수)
    System.out.println(0x4e);
    System.out.println(-0x4e);

    //정수 리터럴 (2진수)
    System.out.println(0b01001110);
    System.out.println(-0b01001110);


    //부동소수 리터럴
    System.out.println(+78);
    System.out.println(-78);


  }
}